    private void jButton_update_hisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_update_hisActionPerformed
        // TODO add your handling code here:
        int x = jTable_history.getSelectedRow();
        if (x > -1) {
            String degree = jTextField_degree_update.getText();
            String res = jTextField_reason_update.getText();
            String date = jTextField_date_update.getText();
            boolean flag_deg = ob.check_int(degree);
            if (flag_deg == false) {
                jTextField_degree_update.setBackground(Color.pink);
            }

            boolean flag_date = true;
            try {
                flag_date = ob.check_time(date, 1);
            } catch (ParseException ex) {
                Logger.getLogger(gui.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (flag_date == false) {
                jTextField_date_update.setBackground(Color.pink);
            }


            if (flag_date == true && flag_deg == true) {
                String id_team = (String) model_history.getValueAt(x, 0);
                String id_stu = (String) model_history.getValueAt(x, 1);
                String deg = (String) model_history.getValueAt(x, 2);


                try {
                    ob.stmt.executeUpdate("update history set degree=" + degree + " , reason='" + res + "' , date='" + date + "' where id_team='"
                            + id_team + "' and id_student='" + id_stu + "'");
                    ResultSet rs = ob.search_student_id_team_students(id_team, id_stu);
                    rs.next();

                    String old_de = rs.getString("total_degress");
                    if (Double.parseDouble(deg) > Double.parseDouble(degree)) {
                           ob.update_total_degree_students(id_team, id_stu, old_de,
                                String.valueOf((Double.parseDouble(deg)-Double.parseDouble(degree))*-1)) ; 
                    // ob.update_total_degree_students(id_team, id_stu, old_de, String.valueOf(Double.parseDouble(degree) * -1));
                    }
                    else 
                    {
                       ob.update_total_degree_students(id_team, id_stu, old_de,
                                String.valueOf((Double.parseDouble(degree)-Double.parseDouble(deg)))) ; 
                    }
                   


                    JOptionPane.showMessageDialog(null, "sucessful operation");
                } catch (SQLException ex) {
                    Logger.getLogger(gui.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_jButton_update_hisActionPerformed

