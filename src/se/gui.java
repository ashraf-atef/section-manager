    private void jButton_add_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_add_insertActionPerformed
        // TODO add your handling code here:

        String id_team = jTextField_is_team.getText();
        String id_student = jTextField_id_student.getText();
        String name = jTextField_name_student.getText();
        String no = jTextField_no_section.getText();
        String dept = jTextField_department.getText();
        boolean flag_no = ob.check_int(no);
        if (flag_no == false) {
            jTextField_no_section.setBackground(Color.pink);
        }
        boolean flag = true;

        ResultSet rs = ob.search_student_id_team_students(id_team, id_student);
        try {
            rs.next();


            if (rs.getString("id_team").length() != 0) {
                flag = false;
                JOptionPane.showMessageDialog(null, "sorry there is a student that has id =" + id_student + " in team =" + id_team);
                jTextField_id_student.setBackground(Color.pink);
                jTextField_is_team.setBackground(Color.pink);
            }
        } catch (Exception e) {
        }

        if (flag == true && flag_no == true) {
            ob.insert_students(id_team, id_student, name, no, dept);
            jTextField_id_student.setText("");
            jTextField_is_team.setText("");
            jTextField_name_student.setText("");
            jTextField_id_student.setBackground(Color.white);
            jTextField_is_team.setBackground(Color.white);
            jTextField_no_section.setBackground(Color.white);
            JOptionPane.showMessageDialog(null, "sucessful operation");
        }




    }//GEN-LAST:event_jButton_add_insertActionPerformed

