    private void jButton_add_insert1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_add_insert1ActionPerformed
        // TODO add your handling code here:
        int x = jTable_students.getSelectedRow();
        String id_team = "";
        try {
            id_team = (String) model_students.getValueAt(x, 0);
        } catch (Exception e) {
        }
        if (id_team.length() != 0) {
            String id_student = (String) model_students.getValueAt(x, 1);
            String total_degree = (String) model_students.getValueAt(x, 5);

            String degree = jTextField_degree.getText();
            String reson = jTextField_reson.getText();
            boolean flag_deg = ob.check_int(degree);
            if (flag_deg == false) {
                jTextField_degree.setBackground(Color.pink);
            }
            boolean flag_primary = true;
            ResultSet rs = ob.search_history_primary(id_team, id_student, ob.time_now());
            try {
                rs.next();
                if (rs.getString("id_team").length() != 0) {
                    flag_primary = false;
                    JOptionPane.showMessageDialog(null, "this student have been taken degree in this day before");
                }
            } catch (Exception e) {
            }
            if (flag_deg == true && flag_primary == true) {
                ob.insert_history(id_team, id_student, degree, reson);
                ob.update_total_degree_students(id_team, id_student, total_degree, degree);

                jTextField_degree.setBackground(Color.white);
                JOptionPane.showMessageDialog(null, "sucessful operation");
            }

        } else {
            JOptionPane.showMessageDialog(null, "please choose a student from table students then try again ");
        }
    }//GEN-LAST:event_jButton_add_insert1ActionPerformed

